func shortestBridge(grid [][]int) (step int) {
	i1, j1 := 0, 0
	for i := 0; i < len(grid); i++ {
		for j := 0; j < len(grid[0]); j++ {
			//使用dfs将其中一个1存储起来
			if grid[i][j] == 1 {
				i1 = i
				j1 = j
				break
			}
		}
	}

	type pair934 struct{ x, y int }
	queue := []pair934{}
	var dfs func(i int, j int)
	dfs = func(i int, j int) {
		if i >= 0 && i < len(grid) && j >= 0 && j < len(grid[0]) {
			if grid[i][j] == 0 {
				return
			}
			if grid[i][j] == 1 {
				grid[i][j] = 2
				dfs(i+1, j)
				dfs(i, j+1)
				dfs(i-1, j)
				dfs(i, j-1)
			}
		}
	}
	dfs(i1, j1)

	for i := 0; i < len(grid); i++ {
		for j := 0; j < len(grid[0]); j++ {
			if grid[i][j] == 1 {
				queue = append(queue, pair934{i, j})
			}
		}
	}

	nextMove := []pair934{{-1, 0}, {1, 0}, {0, -1}, {0, 1}}
	res := 0
	for len(queue) > 0 {
		n := len(queue)
		res++
		for i := 0; i < n; i++ {
			t := queue[0]
			queue = queue[1:]
			for j := 0; j < len(nextMove); j++ {
				nextI := t.x + nextMove[j].x
				nextJ := t.y + nextMove[j].y
				if nextI >= 0 && nextI < len(grid) && nextJ >= 0 && nextJ < len(grid[0]) {
					if grid[nextI][nextJ] == 2 {
						return res - 1
					}
					if grid[nextI][nextJ] == 0 {
						grid[nextI][nextJ] = 1
						queue = append(queue, pair934{nextI, nextJ})
					}
				}
			}
		}
	}

	return -1

}

